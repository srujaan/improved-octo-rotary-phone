public class DoubleArray {
    public static void main(String[] args) {
        double[][] a = new double[3][3];
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                a[i][j] = 0.0;
                System.out.println(a[i][j]);

            }
        } 
        
    }
}